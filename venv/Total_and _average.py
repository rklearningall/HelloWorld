## Write a program to accept the weight of 3 persons, calculate the total weight,
# determine the average weight and display these details.

per1 = float(input(" Enter the person 1 "))
per3 = float(input(" Enter the person 2 "))
per3 = float(input(" Enter the person 3 "))

Sum = per1 + per1 + per3
Average = Sum / 3

print("Total weight of three persons ",Sum)
print("Average weight of three persons ",Average)

